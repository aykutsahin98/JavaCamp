package JavaHomeworkOne;

public class Course {

	public Course (int id, String name, double unitPrice, String instructor, String detail) {
		this.id = id;
		this.name = name;
		this.unitPrice = unitPrice;
		this.instructor = instructor;
		this.detail = detail;
	}
	
	int id;
	String name;
	double unitPrice;
	String instructor;
	String detail;
}
